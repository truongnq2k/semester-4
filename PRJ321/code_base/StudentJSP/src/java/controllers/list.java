    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        String numOfStudent = request.getParameter("num");
        int num;
        try {
            num = Integer.parseInt(numOfStudent);
        } catch (Exception e) {
            num = 0;
        }

        List<Student> ls = new ArrayList<>();
        Random r = new Random();
        String st = "ABCDEFGHIK";
        for (int i = 0; i < num; i++) {
            int id = i + 1;
            boolean gender = r.nextBoolean();
            String name = (gender ? "Mr " : "Mrs ") + st.charAt(r.nextInt(st.length()));
            //int randomNumber = random.nextInt(max + 1 - min) + min;
            int age = r.nextInt(23 + 1 - 18) + 18;
            Student s = new Student(id, name, age, gender);
            ls.add(s);
        }

        request.setAttribute("data", ls);
        //redirect to lisview.jsp with object data
        request.getRequestDispatcher("listview.jsp").forward(request, response);

    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

